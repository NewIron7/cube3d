#include "cube3d.h"

static void	cpy_map(char tmp[10][24], int w, int h, char ***map)
{
	int	x;
	int	y;

	*map = malloc(sizeof(char *) * h);
	y = 0;
	while (y < h)
	{
		x = 0;
		(*map)[y] = malloc(w);
		while (x < w)
		{
			(*map)[y][x] = tmp[y][x];
			x++;
		}
		y++;
	}
}

